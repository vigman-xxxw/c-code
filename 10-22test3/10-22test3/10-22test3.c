#include <stdio.h>



int main()
{
	                //CUP只有加法器 减法是模拟出来的   
					//计算机以补码的形式存储的理由是:   补码可以消除  在计算中符号的影响
	int a = -10;
	//原码10000000000000000000000000001010
	//反码11111111111111111111111111110101
	//补码11111111111111111111111111110110
	int b = 10;
	//正整数的原码、反码、补码 相同
	//补码00000000000000000000000000001010
	int c = a - b;
	//补码的计算
	//补码111111111111111111111111111111110000
	//反码111111111111111111111111111111101111
	

	printf("%d",c);

	return 0;
}