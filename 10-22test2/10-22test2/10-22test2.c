#include <stdio.h>

int main()
{
	char a = -1;
	//原码10000000000000000000000000000001
	//反码11111111111111111111111111111110
	//补码11111111111111111111111111111111
	//char类型只保留补码右边8位
	//打印的时候 整型提升 前面补1
	signed char b = -1;
	//同理char
	unsigned char c = -1;
	//原码10000000000000000000000000000001
	//反码11111111111111111111111111111110
	//补码11111111111111111111111111111111
	//无符号char类型只保留补码右边8位
	//打印的时候 整型提升 前面补0
	//00000000000000000000000011111111
	printf("a=%d,b=%d,c=%d ",a,b,c);

	//-1   -1     255

	return 0;
}